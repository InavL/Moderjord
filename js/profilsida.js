$(document).ready(function () {
    $("#javaBar").mouseover(function () {
        $(".java-info").css({
            "visibility": "visible"
        }); 
});
});
    $(document).ready(function() {
    $("#javaBar").mouseout(function() {
        $(".java-info").css({
            "visibility": "hidden"
        }); 
});    
});   

