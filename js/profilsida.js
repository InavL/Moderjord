$(document).ready(function () {
    $(".innerJava").mouseover(function () {
        $(".java-info").css({
            "visibility": "visible"
        }); 
});
});
    $(document).ready(function() {
    $(".innerJava").mouseout(function() {
        $(".java-info").css({
            "visibility": "hidden"
        }); 
});    
});   

