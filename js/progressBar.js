function pBarAnJava() {
    var bar = document.getElementById("anJavaProg")
    var progress = document.getElementById("progTxtJava")
    var width = 0;
    var x = setInterval(frame, 50);
    isCCalled = true;

    function frame() {
        if (width >= 75) {
            clearInterval(x);
        } else {
            width++
            bar.style.width = width + '%';
            progress.innerHTML = width + '%';
        }
    }
}

